def t(key: str, lang: str = 'ru') -> str:
    """
    מחזיר טקסט מתורגם לפי מפתח ושפה
    
    Args:
        key: מפתח הטקסט
        lang: שפה ('ru' או 'he'), ברירת מחדל: 'ru'
    
    Returns:
        הטקסט המתורגם, או המפתח עצמו אם לא נמצא
    """
    if key in TEXTS:
        text = TEXTS[key].get(lang, TEXTS[key].get('ru', key))
        # הוספת RTL mark לעברית לתמיכה נכונה בכיוון
        if lang == 'he' and text:
            return '\u200F' + text  # RTL Mark
        return text
    return key


def get_user_lang(user_id: int) -> str:
    """
    מחזיר את שפת המשתמש מה-DB - Supabase only
    
    Args:
        user_id: מזהה המשתמש
    
    Returns:
        קוד השפה ('ru' או 'he')
    """
    from db.db import db_client
    
    users = db_client.select('users', {'user_id': user_id})
    if users and users[0].get('lang'):
        return users[0]['lang']
    return 'ru'  # ברירת מחדל

